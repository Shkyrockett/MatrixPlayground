namespace MathematicsNotationLibrary
{
    /// <summary>
    /// 
    /// </summary>
    public enum ComparisonOperators
    {
        /// <summary>
        /// The equals Operator.
        /// </summary>
        Equals,

        /// <summary>
        /// The not equals Operator.
        /// </summary>
        NotEquals,

        /// <summary>
        /// The less than Operator.
        /// </summary>
        LessThan,

        /// <summary>
        /// The greater than Operator.
        /// </summary>
        GreaterThan,

        /// <summary>
        /// The equals or less than Operator.
        /// </summary>
        LessThanOrEquals,

        /// <summary>
        /// The equals or greater than Operator.
        /// </summary>
        GreaterThanOrEquals,

        /// <summary>
        /// The approximate Operator
        /// </summary>
        Approximate,
    }
}
